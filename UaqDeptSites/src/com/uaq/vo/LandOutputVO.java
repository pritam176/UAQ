package com.uaq.vo;

public class LandOutputVO {

	private String status;

	private String status_EN;

	private String status_AR;
	private String requestNo;
	private String satausId;
	private String serviceId;

	public String getRequestNo() {
		return requestNo;
	}

	public void setRequestNo(String requestNo) {
		this.requestNo = requestNo;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getStatus_EN() {
		return status_EN;
	}

	public void setStatus_EN(String status_EN) {
		this.status_EN = status_EN;
	}

	public String getStatus_AR() {
		return status_AR;
	}

	public void setStatus_AR(String status_AR) {
		this.status_AR = status_AR;
	}

	public String getSatausId() {
		return satausId;
	}

	public void setSatausId(String satausId) {
		this.satausId = satausId;
	}

	public String getServiceId() {
		return serviceId;
	}

	public void setServiceId(String serviceId) {
		this.serviceId = serviceId;
	}

}
