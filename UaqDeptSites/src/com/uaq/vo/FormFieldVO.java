package com.uaq.vo;

import java.util.List;

public class FormFieldVO extends BaseVO {

	String text;

	String formFieldType;

	List<String> values;

	List<OptionResultVO> optionsResult;

	/**
	 * @return the text
	 */
	public String getText() {
		return text;
	}

	/**
	 * @param text
	 *            the text to set
	 */
	public void setText(String text) {
		this.text = text;
	}

	/**
	 * @return the formFieldType
	 */
	public String getFormFieldType() {
		return formFieldType;
	}

	/**
	 * @param formFieldType
	 *            the formFieldType to set
	 */
	public void setFormFieldType(String formFieldType) {
		this.formFieldType = formFieldType;
	}

	/**
	 * @return the values
	 */
	public List<String> getValues() {
		return values;
	}

	/**
	 * @param values
	 *            the values to set
	 */
	public void setValues(List<String> values) {
		this.values = values;
	}

	/**
	 * @return the optionsResult
	 */
	public List<OptionResultVO> getOptionsResult() {
		return optionsResult;
	}

	/**
	 * @param optionsResult
	 *            the optionsResult to set
	 */
	public void setOptionsResult(List<OptionResultVO> optionsResult) {
		this.optionsResult = optionsResult;
	}

}
