package com.uaq.vo;

/**
 * VO for Home Page.
 * 
 * @author mraheem
 * 
 */
public class ImageVO extends BaseVO {

	private CategoryVO category;

	private String teaserImage;

	private String genericContentImage;

	private String previewImage;

	private String galleryTeaserImage;

	private String mobileImage;

	private String image;

	private String teaserTitle;

	private String teaserText;
	
	public CategoryVO getCategory() {
		return category;
	}

	public void setCategory(CategoryVO category) {
		this.category = category;
	}

	public String getTeaserImage() {
		return teaserImage;
	}

	public void setTeaserImage(String teaserImage) {
		this.teaserImage = teaserImage;
	}

	public String getGenericContentImage() {
		return genericContentImage;
	}

	public void setGenericContentImage(String genericContentImage) {
		this.genericContentImage = genericContentImage;
	}

	public String getPreviewImage() {
		return previewImage;
	}

	public void setPreviewImage(String previewImage) {
		this.previewImage = previewImage;
	}

	public String getGalleryTeaserImage() {
		return galleryTeaserImage;
	}

	public void setGalleryTeaserImage(String galleryTeaserImage) {
		this.galleryTeaserImage = galleryTeaserImage;
	}

	public String getMobileImage() {
		return mobileImage;
	}

	public void setMobileImage(String mobileImage) {
		this.mobileImage = mobileImage;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getTeaserTitle() {
		return teaserTitle;
	}

	public void setTeaserTitle(String teaserTitle) {
		this.teaserTitle = teaserTitle;
	}

	public String getTeaserText() {
		return teaserText;
	}

	public void setTeaserText(String teaserText) {
		this.teaserText = teaserText;
	}

}
