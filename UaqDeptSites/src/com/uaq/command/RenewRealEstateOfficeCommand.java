package com.uaq.command;

import java.io.Serializable;

import org.springframework.web.multipart.MultipartFile;
/**
 * 
 * @author G Sekhar
 *
 */
public class RenewRealEstateOfficeCommand implements Serializable {

	private String directorName;
	private String address;
	private String phoneNumber;
	private String requestNumber;
	private String myDetails;

	private MultipartFile clearanceCertificate;
	private MultipartFile personalPhotograph;

	public String getDirectorName() {
		return directorName;
	}

	public void setDirectorName(String directorName) {
		this.directorName = directorName;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}

	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	public String getRequestNumber() {
		return requestNumber;
	}

	public void setRequestNumber(String requestNumber) {
		this.requestNumber = requestNumber;
	}

	public String getMyDetails() {
		return myDetails;
	}

	public void setMyDetails(String myDetails) {
		this.myDetails = myDetails;
	}

	public MultipartFile getClearanceCertificate() {
		return clearanceCertificate;
	}

	public void setClearanceCertificate(MultipartFile clearanceCertificate) {
		this.clearanceCertificate = clearanceCertificate;
	}

	public MultipartFile getPersonalPhotograph() {
		return personalPhotograph;
	}

	public void setPersonalPhotograph(MultipartFile personalPhotograph) {
		this.personalPhotograph = personalPhotograph;
	}

}
