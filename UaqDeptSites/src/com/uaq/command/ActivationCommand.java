package com.uaq.command;

public class ActivationCommand {

	private String userType;;

	private String mobileNumber;

	private String emirateId;

	private String passportId;
	
	private String tradelicence;
	
	private String emirate;

	public String getMobileNumber() {
		return mobileNumber;
	}

	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}

	public String getEmirateId() {
		return emirateId;
	}

	public void setEmirateId(String emirateId) {
		this.emirateId = emirateId;
	}

	public String getPassportId() {
		return passportId;
	}

	public void setPassportId(String passportId) {
		this.passportId = passportId;
	}

	public String getUserType() {
		return userType;
	}

	public void setUserType(String userType) {
		this.userType = userType;
	}

	public String getTradelicence() {
		return tradelicence;
	}

	public void setTradelicence(String tradelicence) {
		this.tradelicence = tradelicence;
	}

	public String getEmirate() {
		return emirate;
	}

	public void setEmirate(String emirate) {
		this.emirate = emirate;
	}

}
